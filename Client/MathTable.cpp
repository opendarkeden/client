//-----------------------------------------------------------------------------
// Get Angle To Target
//-----------------------------------------------------------------------------
int MathTable::GetAngleToTarget(int nX, int nY, int nTargetX, int nTargetY)
{
	int nAngle;
	int cx = nTargetX - nX;
	int cy = nTargetY - nY;

	if ( cx == 0 ) {
		if ( cy > 0 ) return ANGLE_270;
		if ( cy < 0 ) return ANGLE_90;
	}

	if ( cy == 0 ) {
		if ( cx > 0 ) return ANGLE_0;
		if ( cx < 0 ) return ANGLE_180;
	}

	if ( cx == 0 && cy == 0 )
		return ANGLE_0;

	if ( cy < cx ) {
		if ( -cy < cx )
			nAngle = ANGLE_0 + FArcTanTab[MAX_ANGLE_HALF + ( MAX_ANGLE_HALF * cy / cx )];
		else
			nAngle = ANGLE_270 - FArcTanTab[MAX_ANGLE_HALF + ( MAX_ANGLE_HALF * cx / cy )];
	} else {
		if ( -cy < cx )
			nAngle = ANGLE_90 - FArcTanTab[MAX_ANGLE_HALF + ( MAX_ANGLE_HALF * cx / cy )];
		else
			nAngle = ANGLE_180 + FArcTanTab[MAX_ANGLE_HALF + ( MAX_ANGLE_HALF * cy / cx )];
	}

	// INVERT angle...
	nAngle = MAX_ANGLE - nAngle;  // ? - -;

	// CLIP angle...
	nAngle = ClipAngle ( nAngle );

    return nAngle;
}

//-----------------------------------------------------------------------------
// Get AngleDir
//-----------------------------------------------------------------------------
// angle targetAngle  
// +/-   ? ( 0, 1, -1 )
//-----------------------------------------------------------------------------
int MathTable::GetAngleDir(int nAngle, int nTargetAngle)
{
	if ( nAngle == nTargetAngle ) return 0;

	int nDir = ( nAngle > nTargetAngle ) ? -1 : 1;
	nDir = ( abs ( nAngle - nTargetAngle ) >= ANGLE_180 ) ? -nDir : nDir;
	return nDir;
}

//-----------------------------------------------------------------------------
// Clip Angle
//-----------------------------------------------------------------------------
int MathTable::ClipAngle(int nAngle)
{
	if ( nAngle < 0 ) 
		nAngle += MAX_ANGLE;
	else if ( nAngle >= MAX_ANGLE )
		nAngle -= MAX_ANGLE;
	return nAngle;
}



